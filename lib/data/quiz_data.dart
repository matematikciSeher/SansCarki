  static List<QuizQuestion> getQuestionsByCategory(QuizCategory category) {
    return getAllQuestions().where((q) => q.category == category).toList();
  }

  static List<QuizQuestion> getRandomQuestions({
    QuizCategory? category,
    int count = 10,
  }) {
    List<QuizQuestion> questions = getAllQuestions();

    if (category != null) {
      questions = questions.where((q) => q.category == category).toList();
    }

    questions.shuffle();
    return questions.take(count).toList();
  }
