class _MathChallengeGameScreenState extends State<MathChallengeGameScreen> with TickerProviderStateMixin {
  late AnimationController _questionAnimationController;
  late Animation<double> _questionFadeAnimation;
  late Animation<Offset> _questionSlideAnimation;

  final List<MathQuestion> _questions = [];
  int _currentQuestionIndex = 0;
  int _correctAnswers = 0;
  int _score = 0;
  Timer? _gameTimer;
  int _remainingTime = 30;
  bool _isAnswered = false;
  int? _selectedAnswerIndex;
  // bool _isGameComplete = false; // kullanƒ±lmƒ±yor
  bool _showInfo = true;

  // Oyun parametreleri
  late int _startTime;

  @override
  void initState() {
    super.initState();
    _initializeGameParams();
    _initializeAnimations();
    _generateQuestions();
    _startTimer();
  }

  void _initializeGameParams() {
    // S√ºre: 30 sn
    _startTime = 30;
    _remainingTime = _startTime;
  }

  void _initializeAnimations() {
    _questionAnimationController = AnimationController(
      duration: const Duration(milliseconds: 600),
      vsync: this,
    );

    _questionFadeAnimation = Tween<double>(
      begin: 0.0,
      end: 1.0,
    ).animate(CurvedAnimation(
      parent: _questionAnimationController,
      curve: Curves.easeInOut,
    ));

    _questionSlideAnimation = Tween<Offset>(
      begin: const Offset(0, 0.2),
      end: Offset.zero,
    ).animate(CurvedAnimation(
      parent: _questionAnimationController,
      curve: Curves.easeOutBack,
    ));

    _questionAnimationController.forward();
  }

  void _generateQuestions() {
    _questions.clear();
    final random = Random();
    // Bankadan 10 soru se√ß: en az 2 zor soru
    final hard = _questionBank.where((q) => q.isHard).toList();
    final normal = _questionBank.where((q) => !q.isHard).toList();
    hard.shuffle(random);
    normal.shuffle(random);
    final selected = <_QuestionDef>[];
    selected.addAll(hard.take(2));
    selected.addAll(normal.take(8));
    selected.shuffle(random);
    for (final def in selected) {
      _questions.add(MathQuestion(
        question: def.question,
        options: def.options,
        correctAnswerIndex: def.correctIndex,
      ));
    }
  }

  // Rastgele √ºretim kaldƒ±rƒ±ldƒ± ‚Äì sabit bankadan se√ßiliyor

  // Zorluk hesaplamasƒ± kaldƒ±rƒ±ldƒ±

  void _startTimer() {
    _gameTimer = Timer.periodic(const Duration(seconds: 1), (timer) {
      if (_remainingTime > 0 && !_isAnswered) {
        setState(() {
          _remainingTime--;
        });
      } else if (_remainingTime <= 0 && !_isAnswered) {
        _handleTimeout();
      }
    });
  }

  void _handleTimeout() {
    setState(() {
      _isAnswered = true;
      // S√ºre doldu: yanlƒ±≈ü say ve 10 puan d√º≈ü
      _score -= 10;
    });

    _showTimeoutDialog();
  }

  void _showTimeoutDialog() {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => AlertDialog(
        title: const Text('‚è∞ S√ºre Doldu!'),
        content: const Text('Bu soru i√ßin s√ºre doldu. Doƒüru cevap g√∂steriliyor.'),
        actions: [
          TextButton(
            onPressed: () async {
              Navigator.pop(context);
              await _nextQuestion();
            },
            child: const Text('Devam Et'),
          ),
        ],
      ),
    );
  }

  void _selectAnswer(int answerIndex) {
    if (_isAnswered) return;

    setState(() {
      _selectedAnswerIndex = answerIndex;
      _isAnswered = true;
    });

    _gameTimer?.cancel();

    final currentQuestion = _questions[_currentQuestionIndex];
    final isCorrect = answerIndex == currentQuestion.correctAnswerIndex;

    if (isCorrect) {
      _correctAnswers++;
      _score += 10;
    } else {
      _score -= 10;
    }

    _showAnswerResult(isCorrect, currentQuestion);
  }

  // Puanlama fonksiyonu kullanƒ±lmƒ±yor
  // int _calculatePoints(MathQuestion question) => 10;

  void _showAnswerResult(bool isCorrect, MathQuestion question) {
    final color = isCorrect ? Colors.green : Colors.red;
    final icon = isCorrect ? Icons.check_circle : Icons.cancel;
    final message = isCorrect ? 'Doƒüru!' : 'Yanlƒ±≈ü!';
    final pointsText = isCorrect ? '+10 puan' : '‚àí10 puan';

    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Row(
          children: [
            Icon(icon, color: Colors.white),
            const SizedBox(width: 12),
            Expanded(
              child: Column(
                mainAxisSize: MainAxisSize.min,
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    message,
                    style: const TextStyle(
                      fontWeight: FontWeight.bold,
                      color: Colors.white,
                    ),
                  ),
                  Text(
                    pointsText,
                    style: const TextStyle(
                      color: Colors.white,
                      fontSize: 12,
                    ),
                  ),
                ],
              ),
            ),
          ],
        ),
        backgroundColor: color,
        duration: const Duration(seconds: 2),
        behavior: SnackBarBehavior.floating,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(10)),
      ),
    );

    Future.delayed(const Duration(milliseconds: 3000), () async {
      if (mounted) {
        await _nextQuestion();
      }
    });
  }

  Future<void> _nextQuestion() async {
    if (_currentQuestionIndex < _questions.length - 1) {
      setState(() {
        _currentQuestionIndex++;
        _isAnswered = false;
        _selectedAnswerIndex = null;
        _remainingTime = _startTime;
      });

      // Yeni soru hemen gelsin
      _questionAnimationController.reset();
      _questionAnimationController.forward();
      _startTimer();
    } else {
      await _endGame();
    }
  }

  Future<void> _endGame() async {
    _gameTimer?.cancel();
    // Oyun bitti

    final accuracy = (_correctAnswers / _questions.length) * 100;

    // Bonus puanlar
    final accuracyBonus = accuracy.round();
    _score += accuracyBonus;

    // √ñnce g√ºncel profili Firestore'dan √ßek
    UserProfile? currentProfile;
    try {
      currentProfile = await UserService.getCurrentUserProfile();
    } catch (e) {
      print('G√ºncel profil √ßekme hatasƒ±: $e');
      currentProfile = widget.profile; // Fallback
    }

    // Profil g√ºncelleme - g√ºncel profili kullan
    final updatedProfile = (currentProfile ?? widget.profile).copyWith(
      points: (currentProfile?.points ?? 0) + _score,
      totalGamePoints: ((currentProfile?.totalGamePoints ?? 0)) + _score,
    );

    // UserProfile'ƒ± Firestore'a kaydet
    await _saveProfile(updatedProfile);

    _showGameCompleteDialog(updatedProfile, accuracy);
  }

  Future<void> _saveProfile(UserProfile profile) async {
    try {
      print('üéÆ MATH CHALLENGE Bƒ∞TTƒ∞ - Puan kaydediliyor...');
      print('   ‚ú® Kazanƒ±lan Puan: $_score');
      print('   üìä Yeni Oyun Puanƒ±: ${profile.totalGamePoints ?? 0}');

      await UserService.updateCurrentUserProfile(profile);
      print('   ‚úÖ Firestore\'a kaydedildi!');

      // Aktivite logla (opsiyonel)
      await UserService.logActivity(
        activityType: 'math_game_completed',
        data: {
          'score': _score,
          'correctAnswers': _correctAnswers,
          'totalQuestions': _questions.length,
        },
      );
    } catch (e) {
      print('‚ùå Matematik oyunu profil kaydetme hatasƒ±: $e');
    }
  }

  void _showGameCompleteDialog(UserProfile updatedProfile, double accuracy) {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => AlertDialog(
        title: const Text('üéâ Tebrikler!'),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Text('Matematik m√ºcadelesini tamamladƒ±n!'),
